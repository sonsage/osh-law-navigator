export function getOfficialArticleNumber(articleNo: string) {
  return articleNo.match(/\d+(?:-\d+)?/)?.[0] ?? "";
}

export function getOfficialArticleUrl(articleNo: string) {
  const articleNumber = getOfficialArticleNumber(articleNo);
  return `https://law.moj.gov.tw/LawClass/LawSingle.aspx?flno=${articleNumber}&pcode=N0060001`;
}

