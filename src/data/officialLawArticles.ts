function article(articleNo: string): OfficialArticle {
  const key = articleNo.replace("第 ", "").replace(" 條", "");
  const known = knownTitles[key];
  return {
    id: `osha-${key}`,
    articleNo,
    title: known?.title ?? "官方條文",
    text: articleTexts[key] ?? [],
    keywords: known?.keywords ?? ["職業安全衛生法", articleNo],
  };
}

function rangeArticles(start: number, end: number) {
  return Array.from({ length: end - start + 1 }, (_, index) => article(`第 ${start + index} 條`));
}

